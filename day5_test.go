package main

import (
	"bytes"
	"strconv"
	"testing"
)

func TestProcessExtend(t *testing.T) {
	tests := []struct {
		input1         []int
		input2         string
		expectedOutput int
	}{
		{[]int{3, 9, 8, 9, 10, 9, 4, 9, 99, -1, 8}, "8", 1},
		{[]int{3, 9, 8, 9, 10, 9, 4, 9, 99, -1, 8}, "9", 0},
		{[]int{3, 9, 7, 9, 10, 9, 4, 9, 99, -1, 8}, "3", 1},
		{[]int{3, 9, 7, 9, 10, 9, 4, 9, 99, -1, 8}, "8", 0},
		{[]int{3, 3, 1108, -1, 8, 3, 4, 3, 99}, "8", 1},
		{[]int{3, 3, 1108, -1, 8, 3, 4, 3, 99}, "1", 0},
		{[]int{3, 3, 1107, -1, 8, 3, 4, 3, 99}, "1", 1},
		{[]int{3, 3, 1107, -1, 8, 3, 4, 3, 99}, "9", 0},
		{[]int{3, 12, 6, 12, 15, 1, 13, 14, 13, 4, 13, 99, -1, 0, 1, 9}, "0", 0},
		{[]int{3, 12, 6, 12, 15, 1, 13, 14, 13, 4, 13, 99, -1, 0, 1, 9}, "10", 1},
		{[]int{3, 3, 1105, -1, 9, 1101, 0, 0, 12, 4, 12, 99, 1}, "2", 1},
		{[]int{3, 3, 1105, -1, 9, 1101, 0, 0, 12, 4, 12, 99, 1}, "0", 0},
		{[]int{3, 21, 1008, 21, 8, 20, 1005, 20, 22, 107, 8, 21, 20, 1006, 20, 31,
			1106, 0, 36, 98, 0, 0, 1002, 21, 125, 20, 4, 20, 1105, 1, 46, 104,
			999, 1105, 1, 46, 1101, 1000, 1, 20, 4, 20, 1105, 1, 46, 98, 99}, "0", 999},
		{[]int{3, 21, 1008, 21, 8, 20, 1005, 20, 22, 107, 8, 21, 20, 1006, 20, 31,
			1106, 0, 36, 98, 0, 0, 1002, 21, 125, 20, 4, 20, 1105, 1, 46, 104,
			999, 1105, 1, 46, 1101, 1000, 1, 20, 4, 20, 1105, 1, 46, 98, 99}, "8", 1000},
		{[]int{3, 21, 1008, 21, 8, 20, 1005, 20, 22, 107, 8, 21, 20, 1006, 20, 31,
			1106, 0, 36, 98, 0, 0, 1002, 21, 125, 20, 4, 20, 1105, 1, 46, 104,
			999, 1105, 1, 46, 1101, 1000, 1, 20, 4, 20, 1105, 1, 46, 98, 99}, "18", 1001},
	}
	for _, tt := range tests {
		temp := &bytes.Buffer{}
		temp.WriteString(tt.input2)
		process(tt.input1, temp, temp)
		result, _ := strconv.Atoi(temp.String())
		if result != tt.expectedOutput {
			t.Errorf("Sum was incorrect, got: %d, want: %d.", result, tt.expectedOutput)
		}
	}
}
